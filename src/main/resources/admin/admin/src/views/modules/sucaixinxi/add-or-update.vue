				sucaijieshao: [
				],
				userid: [
				],
			}
		};
	},
	props: ["parent"],
	computed: {



	},
	created() {
		this.ruleForm.fabushijian = this.getCurDateTime()
	},
	methods: {
		
		// 下载
		download(file){
			window.open(`${file}`)
		},
		// 初始化
		init(id,type) {
			if (id) {
				this.id = id;
				this.type = type;
			}
			if(this.type=='info'||this.type=='else'){
				this.info(id);
			}else if(this.type=='logistics'){
				this.logistics=false;
				this.info(id);
			}else if(this.type=='cross'){
				var obj = this.$storage.getObj('crossObj');
				for (var o in obj){
						if(o=='sucaibianhao'){
							this.ruleForm.sucaibianhao = obj[o];
							this.ro.sucaibianhao = true;
							continue;
						}
						if(o=='sucaimingcheng'){
							this.ruleForm.sucaimingcheng = obj[o];
							this.ro.sucaimingcheng = true;
							continue;
						}
						if(o=='sucaileixing'){
							this.ruleForm.sucaileixing = obj[o];
							this.ro.sucaileixing = true;
							continue;
						}
						if(o=='tupian'){
							this.ruleForm.tupian = obj[o];
							this.ro.tupian = true;
							continue;
						}
						if(o=='sucaiwenjian'){
							this.ruleForm.sucaiwenjian = obj[o];
							this.ro.sucaiwenjian = true;
							continue;
						}
						if(o=='fabushijian'){
							this.ruleForm.fabushijian = obj[o];
							this.ro.fabushijian = true;
							continue;
						}
						if(o=='sucaijieshao'){
							this.ruleForm.sucaijieshao = obj[o];
							this.ro.sucaijieshao = true;
							continue;
